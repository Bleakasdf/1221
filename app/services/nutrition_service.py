def nutrition_text(workout_type: str | None = None) -> str:
    lines = ["Ориентиры на тренировочный день:"]
    lines.extend(f"{key}: {value}" for key, value in MACROS.items())
    lines.append("\nПример дня:")
    lines.extend(f"{meal}: {food}" for meal, food in DAY_EXAMPLE)
    if workout_type in WORKOUT_TYPES:
        lines.append("\nФокус по тренировке:")
        if workout_type == "back":
            lines.append("Спина / Подтягивания: белок + углеводы для восстановления тяговых мышц.")
        elif workout_type == "chest":
            lines.append("Грудь / Жим: восстановление плечевого пояса, груди и трицепса.")
        else:
            lines.append("Калистеника / Ноги: больше углеводов, день энергозатратный.")
    return "\n".join(lines)


